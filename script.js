// Функция отправки сообщения пользователем
function sendMessage() {
    const input = document.getElementById('userInput');
    const messageText = input.value.trim();
    
    if (messageText === '') return;

    // Добавляем сообщение пользователя в чат
    appendMessage(messageText, 'user-message');
    input.value = '';

    // Имитация "размышления" преподавателя
    setTimeout(() => {
        const botResponse = generateInstructorResponse(messageText);
        appendMessage(botResponse, 'bot-message');
    }, 800);
}

// Функция добавления сообщения на экран
function appendMessage(text, className) {
    const chatBox = document.getElementById('chatBox');
    const messageDiv = document.createElement('div');
    messageDiv.className = `message ${className}`;
    
    if (className === 'bot-message') {
        messageDiv.innerHTML = `<strong>Капитан Иванов:</strong> ${text}`;
    } else {
        messageDiv.innerHTML = `${text}`;
    }
    
    chatBox.appendChild(messageDiv);
    chatBox.scrollTop = chatBox.scrollHeight; // Прокрутка вниз
}

